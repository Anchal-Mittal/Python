import math

def d():
       x=1;
       y=math.sqrt(24)
       print(math.acos(x))# arc cosine
       print(math.asin(x))# arc sine
       print(math.atan(x))# arc tan
       print(math.cos(x))# cos value
       print(math.sin(x))#sin value
       print(math.tan(x))#tan value
       print(math.hypot(x,y))#return sqrt(x*x+y*y)
       print(math.atan2(y,x))#tan(y/x) in radian
       print(math.degrees(x))#convert x into degree
       print(math.radians(x))#convert x into radian
d();
    

    
